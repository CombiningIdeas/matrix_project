#pragma once

/**
 * @file tests_output.h
 * @brief Заголовочный файл для тестирования функций вывода матриц.
 * 
 * Этот файл содержит объявления функций, предназначенных для тестирования функций вывода
 * матриц в консоль и сохранения их в файлы. Тестируется корректность работы с матрицами разных размеров,
 * включая пустые матрицы и матрицы с одним элементом.
 * 
 * Тесты выполняются с использованием стандартных функций для работы с файлами и консолью.
 */

/**
 * @brief Тест для вывода матрицы в консоль.
 * 
 * Эта функция проверяет корректность вызова функции `print_matrix`, которая выводит матрицу в консоль.
 * Мы не проверяем точность вывода в консоль, а лишь гарантируем, что функция не вызывает сбоев при выводе.
 */
void test_print_matrix();

/**
 * @brief Тест для сохранения матрицы в файл.
 * 
 * Эта функция проверяет корректность работы функции `save_matrix_to_file`, которая сохраняет матрицу в файл.
 * Мы проверяем создание файла и его содержание, чтобы удостовериться, что сохраненные данные соответствуют матрице.
 */
void test_save_matrix_to_file();

/**
 * @brief Тест для сохранения пустой матрицы в файл.
 * 
 * Эта функция проверяет, что функция `save_matrix_to_file` корректно обрабатывает пустую матрицу
 * и возвращает ошибку при попытке сохранить такую матрицу.
 */
void test_save_empty_matrix_to_file();

/**
 * @brief Тест для сохранения матрицы с одним элементом в файл.
 * 
 * Эта функция проверяет работу функции `save_matrix_to_file` при сохранении матрицы размером 1x1.
 * Мы проверяем корректность сохраненных данных и их соответствие ожиданиям.
 */
void test_save_single_element_matrix_to_file();

/**
 * @brief Главная функция для выполнения всех тестов вывода.
 * 
 * Эта функция запускает все тесты, связанные с выводом матриц и их сохранением в файлы.
 * Она выполняет серию проверок для различных типов матриц.
 * 
 * @return 0 в случае успешного выполнения всех тестов, в противном случае возвращает код ошибки.
 */
int main_tests_output();
